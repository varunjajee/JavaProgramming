package com.muks.oopsandjava.concepts.i_SwappingWorkaround;


class Car {
    int model, no;

    // Constructor
    Car(int model, int no) {
        this.model = model;
        this.no = no;
    }

    public void print() {
        System.out.println("Car no = " + no + ", model = " + model);
    }

}


